// @filename      cloudinary.js
// @desc          To setup cloudinary

// ====================================================================================================
// Packages
// ====================================================================================================
const cloudinary = require('cloudinary').v2;
const config = require('config');

// ====================================================================================================
// Variables
// ====================================================================================================
const { name, apiKey, apiSecret } = config.get('cloudinary');

// ====================================================================================================
// Setup
// ====================================================================================================
cloudinary.config({
  cloud_name: name,
  api_key: apiKey,
  api_secret: apiSecret,
});

module.exports = { cloudinary };
